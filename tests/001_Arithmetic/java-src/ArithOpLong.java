
class ArithmeticOpLong {

    public static long add_long(long a, long b)
    {
        return a + b;
    }

    public static long sub_long(long a, long b)
    {
        return a - b;
    }

    public static long mul_long(long a, long b)
    {
        return a * b;
    }

    public static long div_long(long a, long b)
    {
        return a / b;
    }

    public static long rem_long(long a, long b)
    {
        return a % b;
    }

    public static long and_long(long a, long b)
    {
        return a & b;
    }

    public static long xor_long(long a, long b)
    {
        return a ^ b;
    }

    public static long or_long(long a, long b)
    {
        return a | b;
    }
}
