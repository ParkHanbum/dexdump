
class ArithmeticOpInt {
    public static int add_int(int a, int b)
    {
        return a + b;
    }

    public static int sub_int(int a, int b)
    {
        return a - b;
    }

    public static int mul_int(int a, int b)
    {
        return a * b;
    }

    public static int div_int(int a, int b)
    {
        return a / b;
    }

    public static int rem_int(int a, int b)
    {
        return a % b;
    }

    public static int and_int(int a, int b)
    {
        return a & b;
    }

    public static int xor_int(int a, int b)
    {
        return a ^ b;
    }

    public static int or_int(int a, int b)
    {
        return a | b;
    }
}
